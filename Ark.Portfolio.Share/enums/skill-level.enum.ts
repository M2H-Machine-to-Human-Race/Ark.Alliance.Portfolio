/**
 * Skill proficiency level enumeration.
 * Represents the level of expertise in a particular skill.
 */
export enum SkillLevel {
    /** Basic understanding, limited practical experience */
    BEGINNER = 'Beginner',
    /** Working knowledge, can apply with guidance */
    INTERMEDIATE = 'Intermediate',
    /** Strong proficiency, can work independently */
    ADVANCED = 'Advanced',
    /** Deep expertise, can mentor others */
    EXPERT = 'Expert'
}
